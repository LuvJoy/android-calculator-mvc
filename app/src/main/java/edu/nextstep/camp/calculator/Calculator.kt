package edu.nextstep.camp.calculator

class Calculator {

    fun plus(number1: Int, number2: Int) = number1 + number2

    fun minus(number1: Int, number2: Int) = number1 - number2

    fun multiply(number1: Int, number2: Int) = number1 * number2

    fun divide(number1: Int, number2: Int) = number1 / number2
}
